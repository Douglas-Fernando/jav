package Fundamentos;

public class AreaCircunferencia {
	public static void main(String[] args) {
//		int raio = 3;
		double raio = 3.4;
		final double PI = 3.14159; // o 'final' representa que essa variavel não pode ser mudado e deve ser representado com letra maiuscula
		double area = PI * raio * raio;
		System.out.println("Área = " + area + "m2.");
		
	}

}
